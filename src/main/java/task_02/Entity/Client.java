package task_02.Entity;

public class Client {
    private long id_clients;
    private String name;
    private int age;
    private String phone;

    public long getId_clients() {
        return id_clients;
    }

    public void setId_clients(long id_clients) {
        this.id_clients = id_clients;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

}



